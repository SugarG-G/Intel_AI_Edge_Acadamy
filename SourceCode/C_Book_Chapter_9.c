// [9-3] 포인터를 사용한 두 정수의 합과 평균 계산
/***********************************************************/

#if 0
#include <stdio.h>

int main(void)
{
	int a = 10, b = 15;
    int *pa, *pb;

    int total;
    int *pt;
    
    double avg;
    double *pg;


    // 1. pa, pb, pt, pg를 초기화.
    pa = &a, pb = &b, pt = &total, pg = &avg;
    // total에 a+b대입
    *pt = *pa + *pb;
    *pg = (double)*pt / 2;
    printf("두 정수의 값 : %d, %d\n", *pa, *pb);
    printf("두 정수의 총합 : %d\n", *pt);
    printf("두 정수의 평균 : %.1lf\n", *pg);
    

	return 0;
}
#endif

