    // 1. 정수형 변수 b와 c를 선언(int로)
    // 2. 실수형 변수 da 선언 (double로)
    // 3. 문자형 변수 ch 선언 (char로)
    // 4. a에 들어 있는 값으로 b를 초기화
    // 5. a에 들어 있는 값에 20을 더해 c를 초기화
